
metros = float(input("Ingrese la cantidad de metros que quiere convertir: "))

kilometros = metros * 0.001
centimetros = metros * 100
milimetros = metros * 1000

print(f"{metros} metros son {kilometros} kilometros")
print(f"{metros} metros son {centimetros} centimetros")
print(f"{metros} metros son {milimetros} milimetros")

