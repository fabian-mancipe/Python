print ("Calcular el promedio de 5 numero")

numero_1 = int(input("Ingrese el primer numero: "))
numero_2 = int(input("Ingrese el segundo numero: "))
numero_3 = int(input("Ingrese el tercero numero: "))
numero_4 = int(input("Ingrese el cuarto numero: "))
numero_5 = int(input("Ingrese el quinto numero: "))

promedio = (numero_1 + numero_2 + numero_3 + numero_4 + numero_5) / 5

print(f"El promedio de los 5 numeros son: {promedio} ")


