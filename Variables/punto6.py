print ("Calcular el 30%, 60% y 90% de un numero")

numero = float(input("Ingrese el numero el cual quiere clacular: "))

resultado_1 = (numero * 30)/100
resultado_2 = (numero * 60)/100
resultado_3 = (numero * 90)/100


print (F"Los porcentajes del numero {numero} son:   el 30% es {resultado_1}, el 60%  es {resultado_2}, el 90% es {resultado_3}")